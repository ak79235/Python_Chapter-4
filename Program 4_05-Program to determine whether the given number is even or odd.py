# WAP to find whether a the given number is even or odd.
print("Program to find whether the entered number is even or odd.\n\n\n")
num = int(input("Enter any number : "))
if num%2==0:
    print("\nEntered number is even.")
else:
    print("\nEntered number is odd.")
